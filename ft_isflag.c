#include "ft_printf.h"

int		ft_isflag(char f)
{
	if (f == ' ' || f == '0' || f == '#' || f == '+' || f == '-')
		return (1);
	else
		return (0);
}

int		ft_ismod(char f)
{
	if (f == 'h' || f == 'l' || f == 'j' || f == 'z')
		return (1);
	else
		return (0);
}
