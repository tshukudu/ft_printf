#include "ft_printf.h"

int	ft_isformat(char f)
{
	if (f == 's' || f == 'S' || f == 'p' || f == 'd' || f == 'D' || f == 'i' ||
			f == 'o' || f == 'O' || f == 'u' || f == 'U' || f == 'x' ||
			f == 'X' || f == 'c' || f == 'C')
		return (1);
	else
		return (0);
}
